public class Livro {
    int id;
    String titulo;
    String autor;
    int quantidade;
    double preco;

    Livro(int id, String titulo, String autor, int quantidade, double preco) {
        this.id = id;
        this.titulo = titulo;
        this.autor = autor;
        this.quantidade = quantidade;
        this.preco = preco;
    }
}